def write_paper_table(f, day_content: dict, to_web: bool, use_title: bool) -> None:
    """Write a paper table for a single topic."""
    if use_title:
        if not to_web:
            f.write("|Publish Date|Title|Authors|PDF|Translate|Read|Code|\n|---|---|---|---|---|---|---|\n")
        else:
            f.write("| Publish Date | Title | Authors | PDF | Translate | Read | Code |\n")
            f.write("|:---------|:-----------------------|:---------|:------|:------|:------|:------|\n")

    for _, v in sort_papers(day_content).items():
        if v is not None:
            line = render_paper_row(v, emphasize=False) if isinstance(v, dict) else str(v)
            f.write(pretty_math(line))


def write_monthly_archive(topic: str, day_content: dict, topic_meta: dict,
                          to_web: bool, use_title: bool) -> None:
    """Write per-topic monthly archive files under docs/."""
    if not os.path.exists("docs"):
        os.makedirs("docs")

    kw = topic.replace(" ", "_")
    grouped = group_papers_by_month(day_content)
    total_papers = sum(len(month_items) for month_items in grouped.values())
    months_sorted = sorted(grouped.items(), reverse=True)
    latest_month, latest_items = months_sorted[0]
    meta = topic_meta.get(topic, {})
    lane_title = meta.get("lane_title", "Research Track")
    lane_eyebrow = meta.get("lane_eyebrow", "Topic Lane")
    prev_topic = meta.get("prev")
    next_topic = meta.get("next")

    with open(f"docs/{kw}.md", "w+") as f_sub:
        f_sub.write(f"## {topic}\n\n")
        f_sub.write(f"Lane: **{lane_title}**\n\n")
        f_sub.write(f"Total papers: **{total_papers}**\n\n")
        f_sub.write(f"Latest archive month: **{latest_month}** ({len(latest_items)} papers)\n\n")

        if prev_topic or next_topic:
            f_sub.write("Topic neighbors: ")
            parts = []
            if prev_topic:
                prev_href = prev_topic.replace(" ", "_") + ".md"
                parts.append(f"[← {prev_topic}]({prev_href})")
            if next_topic:
                next_href = next_topic.replace(" ", "_") + ".md"
                parts.append(f"[{next_topic} →]({next_href})")
            f_sub.write(" · ".join(parts) + "\n\n")

        f_sub.write(f"### {lane_eyebrow}\n\n")
        f_sub.write(f"This page is the monthly archive hub for **{topic}**. Start with the latest month if you want the freshest papers, or move down the list when you are tracing the topic over time.\n\n")
        f_sub.write("## Monthly Archives\n\n")

        for month, month_items in sorted(grouped.items(), reverse=True):
            month_dir = os.path.join("docs", kw)
            os.makedirs(month_dir, exist_ok=True)
            month_file = os.path.join(month_dir, f"{month}.md")
            month_href = f"{kw}/{month}.md"
            f_sub.write(f"- [{month}]({month_href}) ({len(month_items)} papers)\n")

            with open(month_file, "w+") as month_sub:
                month_sub.write(f"## {topic} - {month}\n\n")
                month_sub.write(f"Topic lane: **{lane_title}**\n\n")
                month_sub.write(f"Monthly papers: **{len(month_items)}**\n\n")
                if month == latest_month:
                    month_sub.write("This is the latest archive slice for this topic.\n\n")
                else:
                    month_sub.write(f"Latest available archive for this topic: **{latest_month}**\n\n")
                if use_title:
                    if not to_web:
                        month_sub.write("|Publish Date|Title|Authors|PDF|Translate|Read|Code|\n|---|---|---|---|---|---|---|\n")
                    else:
                        month_sub.write("| Publish Date | Title | Authors | PDF | Translate | Read | Code |\n")
                        month_sub.write("|:---------|:-----------------------|:---------|:------|:------|:------|:------|\n")

                for _, v in sort_papers(month_items).items():
                    if v is not None:
                        line = render_paper_row(v, emphasize=False) if isinstance(v, dict) else str(v)
                        month_sub.write(pretty_math(line))

                month_sub.write(f"\n<p align=right>(<a href=../{kw}.md>back to {topic}</a>)</p>\n\n")

        back_target = "index.md" if to_web else "../README.md"
        f_sub.write(f"\n<p align=right>(<a href={back_target}>back to main</a>)</p>\n\n")


