def pretty_math(s: str) -> str:
    """Process ALL math expressions ($...$) in the string.

    Strips leading/trailing whitespace inside each $...$ block and
    ensures proper spacing around math expressions.
    """
    result = ""
    last_end = 0
    for match in re.finditer(r"\$.*?\$", s):
        start, end = match.span()
        # Append text before this match
        result += s[last_end:start]
        # Determine spacing
        space_before = ""
        if start > 0 and s[start - 1] not in (" ", "*"):
            space_before = " "
        space_after = ""
        if end < len(s) and s[end] not in (" ", "*"):
            space_after = " "
        inner = match.group()[1:-1].strip()
        result += f"{space_before}${inner}${space_after}"
        last_end = end
    result += s[last_end:]
    return result if result else s


def sort_papers(papers: dict) -> dict:
    """Sort papers by key in reverse order (newest first)."""
    output = {}
    keys = sorted(papers.keys(), reverse=True)
    for key in keys:
        output[key] = papers[key]
    return output


def group_papers_by_month(papers: dict) -> dict:
    """Group papers by YYYY-MM date string."""
    grouped = defaultdict(dict)
    for paper_id, entry in sort_papers(papers).items():
        record = ensure_paper_record(entry, paper_id=paper_id)
        grouped[record["date"][:7]][paper_id] = record
    return dict(grouped)


